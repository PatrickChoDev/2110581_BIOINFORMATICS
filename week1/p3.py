def codon_extractor(line: str) -> str:
    return_string = ""
    start = line.find("AUG")
    if start == -1:
        return return_string
    for i in range(start, len(line), 3):
        codon = line[i : i + 3]
        if len(codon) != 3 or codon not in CODON_TABLE or CODON_TABLE[codon] == -1:
            return return_string
        return_string += CODON_TABLE[codon]
    return return_string


